#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*r;
	size_t	i;

	i = 0;
	if (!(r = malloc(sizeof(char) * (size + 1))))
		return (NULL);
	while (i != size + 1)
		r[i++] = '\0';
	return (r);
}
