#include "libft.h"

void	ft_print2dstr(char **str)
{
	int i;

	i = 0;
	if (!str)
		return ;
	while (str[i][0])
	{
		ft_putstr((const char *)str[i]);
		ft_putchar('\n');
		i++;
	}
}
