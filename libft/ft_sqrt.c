int		ft_sqrt(int nb)
{
	int i;

	if (nb <= 0)
		return (0);
	i = 1;
	while (i * i != nb && i < nb / 2 && i < 46341)
		i++;
	if (i * i == nb)
		return (i);
	return (0);
}
